CREATE TABLE IF NOT EXISTS suppliers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workshop_id BIGINT NOT NULL,
    name VARCHAR(160) NOT NULL,
    document VARCHAR(20),
    contact_name VARCHAR(120),
    phone VARCHAR(40),
    email VARCHAR(160),
    category VARCHAR(80),
    address VARCHAR(255),
    notes TEXT,
    status VARCHAR(20) NOT NULL DEFAULT 'active',
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

ALTER TABLE suppliers
    ADD COLUMN IF NOT EXISTS workshop_id BIGINT,
    ADD COLUMN IF NOT EXISTS name VARCHAR(160),
    ADD COLUMN IF NOT EXISTS document VARCHAR(20),
    ADD COLUMN IF NOT EXISTS contact_name VARCHAR(120),
    ADD COLUMN IF NOT EXISTS phone VARCHAR(40),
    ADD COLUMN IF NOT EXISTS email VARCHAR(160),
    ADD COLUMN IF NOT EXISTS category VARCHAR(80),
    ADD COLUMN IF NOT EXISTS address VARCHAR(255),
    ADD COLUMN IF NOT EXISTS notes TEXT,
    ADD COLUMN IF NOT EXISTS status VARCHAR(20) DEFAULT 'active',
    ADD COLUMN IF NOT EXISTS created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
    ADD COLUMN IF NOT EXISTS updated_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP;

UPDATE suppliers SET status = 'active' WHERE status IS NULL;
UPDATE suppliers SET created_at = CURRENT_TIMESTAMP WHERE created_at IS NULL;
UPDATE suppliers SET updated_at = CURRENT_TIMESTAMP WHERE updated_at IS NULL;

ALTER TABLE suppliers
    ALTER COLUMN status SET NOT NULL,
    ALTER COLUMN created_at SET NOT NULL,
    ALTER COLUMN updated_at SET NOT NULL;

CREATE TABLE IF NOT EXISTS financial_transactions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workshop_id BIGINT,
    type VARCHAR(20),
    status VARCHAR(20) DEFAULT 'pending',
    description VARCHAR(180),
    category VARCHAR(80),
    payment_method VARCHAR(80),
    amount NUMERIC(14, 2) DEFAULT 0,
    due_date DATE,
    paid_at TIMESTAMPTZ,
    supplier_id BIGINT,
    service_order_id BIGINT,
    notes TEXT,
    created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP
);

ALTER TABLE financial_transactions
    ADD COLUMN IF NOT EXISTS workshop_id BIGINT,
    ADD COLUMN IF NOT EXISTS type VARCHAR(20),
    ADD COLUMN IF NOT EXISTS status VARCHAR(20) DEFAULT 'pending',
    ADD COLUMN IF NOT EXISTS description VARCHAR(180),
    ADD COLUMN IF NOT EXISTS category VARCHAR(80),
    ADD COLUMN IF NOT EXISTS payment_method VARCHAR(80),
    ADD COLUMN IF NOT EXISTS amount NUMERIC(14, 2) DEFAULT 0,
    ADD COLUMN IF NOT EXISTS due_date DATE,
    ADD COLUMN IF NOT EXISTS paid_at TIMESTAMPTZ,
    ADD COLUMN IF NOT EXISTS supplier_id BIGINT,
    ADD COLUMN IF NOT EXISTS service_order_id BIGINT,
    ADD COLUMN IF NOT EXISTS notes TEXT,
    ADD COLUMN IF NOT EXISTS created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
    ADD COLUMN IF NOT EXISTS updated_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP;

UPDATE financial_transactions SET status = 'pending' WHERE status IS NULL;
UPDATE financial_transactions SET amount = 0 WHERE amount IS NULL;
UPDATE financial_transactions SET created_at = CURRENT_TIMESTAMP WHERE created_at IS NULL;
UPDATE financial_transactions SET updated_at = CURRENT_TIMESTAMP WHERE updated_at IS NULL;

UPDATE financial_transactions
SET type = CASE
    WHEN LOWER(type) IN ('income', 'receita', 'entrada') THEN 'income'
    WHEN LOWER(type) IN ('expense', 'despesa', 'saida', 'saída') THEN 'expense'
    ELSE 'expense'
END
WHERE type IS NULL OR LOWER(type) NOT IN ('income', 'expense');

UPDATE financial_transactions SET description = 'Lançamento financeiro'
WHERE description IS NULL OR TRIM(description) = '';

UPDATE financial_transactions
SET status = CASE
    WHEN LOWER(status) IN ('paid', 'pago', 'recebido', 'quitado') THEN 'paid'
    WHEN LOWER(status) IN ('canceled', 'cancelado') THEN 'canceled'
    ELSE 'pending'
END
WHERE status IS NOT NULL;

ALTER TABLE financial_transactions
    ALTER COLUMN type SET NOT NULL,
    ALTER COLUMN description SET NOT NULL,
    ALTER COLUMN status SET NOT NULL,
    ALTER COLUMN amount SET NOT NULL,
    ALTER COLUMN created_at SET NOT NULL,
    ALTER COLUMN updated_at SET NOT NULL;

CREATE INDEX IF NOT EXISTS idx_suppliers_workshop_name
    ON suppliers (workshop_id, LOWER(name));

CREATE INDEX IF NOT EXISTS idx_suppliers_workshop_status
    ON suppliers (workshop_id, status);

CREATE UNIQUE INDEX IF NOT EXISTS uk_suppliers_workshop_document
    ON suppliers (workshop_id, document)
    WHERE document IS NOT NULL AND document <> '';

CREATE INDEX IF NOT EXISTS idx_financial_transactions_workshop_due_date
    ON financial_transactions (workshop_id, due_date DESC);

CREATE INDEX IF NOT EXISTS idx_financial_transactions_workshop_status
    ON financial_transactions (workshop_id, status);

CREATE INDEX IF NOT EXISTS idx_financial_transactions_workshop_type
    ON financial_transactions (workshop_id, type);
