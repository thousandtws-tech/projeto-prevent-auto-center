CREATE TABLE IF NOT EXISTS suppliers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workshop_id BIGINT NOT NULL,
    name VARCHAR(160) NOT NULL,
    document VARCHAR(20),
    contact_name VARCHAR(120),
    phone VARCHAR(40),
    email VARCHAR(160),
    category VARCHAR(80),
    address VARCHAR(255),
    notes TEXT,
    status VARCHAR(20) NOT NULL DEFAULT 'active',
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_suppliers_workshop_name
    ON suppliers (workshop_id, LOWER(name));

CREATE INDEX IF NOT EXISTS idx_suppliers_workshop_status
    ON suppliers (workshop_id, status);

CREATE UNIQUE INDEX IF NOT EXISTS uk_suppliers_workshop_document
    ON suppliers (workshop_id, document)
    WHERE document IS NOT NULL AND document <> '';

CREATE TABLE IF NOT EXISTS financial_transactions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workshop_id BIGINT NOT NULL,
    type VARCHAR(20) NOT NULL,
    status VARCHAR(20) NOT NULL DEFAULT 'pending',
    description VARCHAR(180) NOT NULL,
    category VARCHAR(80),
    payment_method VARCHAR(80),
    amount NUMERIC(14, 2) NOT NULL DEFAULT 0,
    due_date DATE,
    paid_at TIMESTAMPTZ,
    supplier_id BIGINT REFERENCES suppliers (id) ON DELETE SET NULL,
    service_order_id BIGINT REFERENCES service_orders (id) ON DELETE SET NULL,
    notes TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ck_financial_transactions_type CHECK (type IN ('income', 'expense')),
    CONSTRAINT ck_financial_transactions_status CHECK (status IN ('pending', 'paid', 'canceled'))
);

CREATE INDEX IF NOT EXISTS idx_financial_transactions_workshop_due_date
    ON financial_transactions (workshop_id, due_date DESC);

CREATE INDEX IF NOT EXISTS idx_financial_transactions_workshop_status
    ON financial_transactions (workshop_id, status);

CREATE INDEX IF NOT EXISTS idx_financial_transactions_workshop_type
    ON financial_transactions (workshop_id, type);
